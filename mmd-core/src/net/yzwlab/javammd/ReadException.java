public class ReadException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public ReadException() {
		super();
	}

	public ReadException(Throwable ex) {
		super(ex);
	}

	public ReadException(String message) {
		super(message);
	}

}
